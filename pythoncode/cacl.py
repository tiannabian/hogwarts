class Calculator:
    """这是一个计算器类，包含了加减乘除功能"""

    def add(self, a, b):
        """这个一个加法"""
        return a + b

    def sub(self, a, b):
        """这是一个减法"""
        return a - b

    def multi(self,a, b):
        """这是一个乘法"""
        return a * b

    def div(self,a,b):
        """这是一个除法"""
        return a/b